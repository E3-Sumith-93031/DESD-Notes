# Format string / F-string
#  prefix f before string
# string will contain place holder(s) - {}
# all place holders are replaced by variable/expression value 

num = 10
# print(num)
# print("num = ", num)
print(f"num = {num}")

ch = 'A'
# print(ch)
# print("ch = ", ch)
print(f"ch = {ch}")

str = "sunbeam"
# print(str)
# print("str = ", str)
print(f"str = {str}")

pi = 3.142
# print(pi)
# print("pi = ", pi)
print(f"pi = {pi}")
